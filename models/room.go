type Room struct {
	Id      int64          `orm:"pk" json:"id"`
	Name    string         `orm:"size(20)" json:"name"`
	Clients *SyncClientMap `orm:"-" json:"members"`
	In      chan *Message  `orm:"-" json:"-"`
}
type RoomSet struct {
	Data map[int64]*Room
	Lock sync.RWMutex
}

func NewRoom(name string) *Room {
	room := new(Room)
	room.Name = name
	room.Clients = NewSyncClientMap()
	room.In = make(chan *Message, 100)
	go func() {
		for {
			select {
			case msg := <-room.In:
				room.Clients.Lock()
				for _, v := range room.Clients.Data {
					v.Out <- msg
				}
				room.Clients.UnLock()
			}
		}

	}()
	go func() {
		switch room.Id {
		case 1:
			randomMessage(room, "https://api.erong28.com/api/lottery/latest/period/short", 1, 1, 210)
		case 2:
			randomMessage(room, "https://api.erong28.com/api/lottery/latest/period/short", 2, 20, 210)
		case 3:
			randomMessage(room, "https://api.erong28.com/api/lottery/latest/period/long", 3, 1, 300)
		case 4:
			randomMessage(room, "https://api.erong28.com/api/lottery/latest/period/long", 4, 20, 300)
		default:

		}

	}()
	return room
}

func randomMessage(room *Room, url string, roomid, sleepRange int, interval int64) {
	var sleepTime int
	var difference int64
	for {
		sleepTime = getRandNum(sleepRange) + 1
		difference = time.Now().Unix() - 1553834100
		if difference%interval < 5 || difference%interval > (interval-10) {
			continue
		}
		time.Sleep(time.Duration(sleepTime) * time.Second)
		RandomData(room, roomid, url)
	}
}
func RandomData(room *Room, roomid int, apiUrl string) {
	req, err := http.NewRequest("GET", apiUrl, nil)
	if err != nil {
		return
	}
	transport := &http.Transport{
		Dial: (&net.Dialer{
			Timeout: 5 * time.Second,
		}).Dial,
		MaxIdleConns:    2,
		TLSClientConfig: &tls.Config{InsecureSkipVerify: true},
	}
	client := &http.Client{
		Timeout:   time.Second * 5,
		Transport: transport,
	}
	resp, err := client.Do(req)
	if err != nil {
		return
	}
	if resp == nil {
		return
	}
	defer resp.Body.Close()
	if resp.StatusCode != 200 {
		return
	}
	body, err := ioutil.ReadAll(resp.Body)
	if err != nil {
		return
	}
	type respData struct {
		Data int
	}
	data := &respData{}
	if err = json.Unmarshal(body, data); err != nil {
		return
	}
	room.In <- &Message{GROUPCHAT, getRandonUser(roomid), "第" + strconv.Itoa(data.Data) + `期：
` + getText() + "/" + strconv.Itoa(getAmount())}
}

